    private void jtBuscarUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtBuscarUsuarioMouseClicked
        // TODO add your handling code here:
        frmUsuario frmUsu = new frmUsuario();
        daoEmpleado daoEmp = new daoEmpleado();
        empleado empl = new empleado();
        try{
            filaseleccionada = jtBuscarUsuario.getSelectedRow();        
            if(filaseleccionada == -1){
                JOptionPane.showMessageDialog(this,"No se ha seleccionado ninguna fila","Mensaje del Sistema",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                //DefaultTableModel modelotabla = (DefaultTableModel) jtCategoria.getModel();
                
                frmUsu.txtUsuarioEmpleado.setText(jtBuscarUsuario.getValueAt(filaseleccionada,1).toString());  
                frmUsu.txtIdUsuario.setText(jtBuscarUsuario.getValueAt(filaseleccionada,0).toString());  
                empl = daoEmp.obtenerEmpleado(jtBuscarUsuario.getValueAt(filaseleccionada,3).toString());
                frmUsuario.emp = empl;
                JOptionPane.showMessageDialog(this,"¡Categoría encontrada!","Mensaje del Sistema",JOptionPane.INFORMATION_MESSAGE); 
                frmUsu.txtempleado.setText(empl.getNombre() +" " + empl.getApellidop()+" " + empl.getApellidom());
                frmUsu.show();
//                frmcatpla.btnActualizar.setEnabled(true);
//                frmcatpla.btnBorrar.setEnabled(true);
//                frmcatpla.txtnombreCategoria.setEnabled(true);
                dispose();
                                            
            }
        }catch (HeadlessException ex){
            JOptionPane.showMessageDialog(this,"Error" + ex + "\nPor favor inténtelo nuevamente","Mensaje del Sistema",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtBuscarUsuarioMouseClicked

