    private void btnAnularFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnularFacturaActionPerformed
        int fila=tblReporteFac.getSelectedRow();
        String anular;
        
        anular=tblReporteFac.getValueAt(fila, 0).toString();
        int msg=JOptionPane.showConfirmDialog(this, "Desea Hacer cambios en el registro", "Grabar", JOptionPane.YES_NO_OPTION);
            if(msg==JOptionPane.YES_OPTION){
                vfacturar f = fact.buscarFactura(Integer.parseInt(anular));
                if(f!=null){
                    f.setEstado("anulado");
                    
                    fact.grabar();//Grabar datos al archivo
                    listarFacturacion();
                }
            }
    }//GEN-LAST:event_btnAnularFacturaActionPerformed

